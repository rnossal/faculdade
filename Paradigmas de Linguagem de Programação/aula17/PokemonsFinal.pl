/* A partir do nome do Pokemon apresente as caracteristicas basicas dele: altura, peso e sexo? */
caracteristicas(Pk):-
	altura(Pk, A),
	peso(Pk, P),
	sexo(Pk, S),
	write('Altura '),
	write(A),
	nl,
	write('Peso: '),
	write(P),
	nl,
	write('Sexo: '),
	write(S),
	nl,
	fail
	;
	true.

/* Quais as habilidades do Pokemon? */
habilidades(Pk):-
	habilidade(Pk, H),
	write('Habilidade: '),
	write(H),
	nl,
	fail
	;
	true.

/* Quais os Pokemons possuem a habilidade H? */
possuem_habilidade(H):-
	habilidade(Pk, H),
	write(Pk),
	nl,
	fail
	;
	true.

/* Quais Pokemons podem evoluir? (Apresente a evolucao) */
pode_evoluir():-
	evolucao(P, E),
	write(P),
	write(' => '),
	write(E),
	nl,
	fail
	;
	true.

/* Quais Pokemons apresentam duas ou mais evolucoes? (Apresente as evolucoes) */
qtd_evolucoes(P, 0).
qtd_evolucoes(P, Q):-
	evolucao(P, E),
	qtd_evolucoes(E, Q1),
	Q is Q1 + 1.

duas_mais_evolucoes():-
	evolucao(P, E),
	qtd_evolucoes(P, Q),
	Q >= 2,
	write(Q),
	loop_evolucoes(P).

loop_evolucoes(P):-
	evolucao(P, E),
	write(P),
	write(' => '),
	write(E),
	nl,
	loop_evolucoes(E).

